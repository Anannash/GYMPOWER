        int  CY = this.getLocation().y;
        
        
        int MoveX =((CX+evt.getX()) - (CX+mPoint.x));
        int MoveY =((CY+evt.getY()) - (CY+mPoint.y));

        
        int x = CX+MoveX;
        int y = CY+MoveY;
        
        
        this.setLocation(x, y);
