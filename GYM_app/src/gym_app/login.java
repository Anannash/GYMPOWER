    private void ImagenLabel(String URL, ImageIcon x, ImageIcon imagen, JLabel label) {
    // Crea un ImageIcon a partir de la URL
    x = new ImageIcon(URL);

    // Escala la imagen para que se ajuste al tamaño del JLabel
    Image images = x.getImage().getScaledInstance(label.getWidth(), label.getHeight(), Image.SCALE_SMOOTH);

    // Crea un nuevo ImageIcon con la imagen escalada
    imagen = new ImageIcon(images);

    // Establece el ImageIcon en el JLabel
    label.setIcon(imagen);
}
    
     private void SetImageButton(String url, JButton boton) {
        ImageIcon image = new ImageIcon(url);
        Icon icon = new ImageIcon(image.getImage().getScaledInstance(boton.getWidth(), boton.getHeight(), Image.SCALE_SMOOTH));
        boton.setIcon(icon);

        boton.setBorderPainted(false);
        boton.setContentAreaFilled(false);
        boton.setFocusPainted(false);
        boton.setOpaque(false);
        this.repaint();
    }

